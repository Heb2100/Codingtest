SELECT *, COUNT(R.MEMBER_ID)
FROM REST_REVIEW R LEFT JOIN MEMBER_PROFILE M ON R.MEMBER_ID=M.MEMBER_ID
GROUP BY R.MEMBER_ID
ORDER BY COUNT(R.MEMBER_ID) DESC

SELECT R.MEMBER_ID, COUNT(R.MEMBER_ID)
FROM REST_REVIEW R LEFT JOIN MEMBER_PROFILE M ON R.MEMBER_ID=M.MEMBER_ID
GROUP BY R.MEMBER_ID
ORDER BY COUNT(R.MEMBER_ID) DESC
LIMIT 1

SELECT *
FROM REST_REVIEW R LEFT JOIN MEMBER_PROFILE M ON R.MEMBER_ID = M.MEMBER_ID
WHERE R.MEMBER_ID IN (
    SELECT R.MEMBER_ID
    FROM REST_REVIEW R LEFT JOIN MEMBER_PROFILE M ON R.MEMBER_ID=M.MEMBER_ID
    GROUP BY R.MEMBER_ID
    ORDER BY COUNT(R.MEMBER_ID) DESC LIMIT 1
)